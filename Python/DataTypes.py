#Type of Data Types in python
#Text Type: Str
#Numeric Type: Int, Float, Complex
#Sequence Types: List, Tuple, Range
#Mapping Types: Dict
#Set Types : Set, Frozenset
#Boolean Type: Bool
#Binary Types: Bytes, Bytearray, Memoryview

#Wethe can use the type() function to see the type of data type

x = "Hello World"
print(type(x)) # Str

x = 5
print(type(x)) # Int

x = 5.5
print(type(x)) # Float

x = ["apple", "banana", "cherry"]
print(type(x)) # List

x = ("apple", "banana", "cherry")
print(type(x)) # Tuple

x = range(6)
print(type(x)) # Range

x = True
print(type(x)) # Bool

